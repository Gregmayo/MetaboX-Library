namespace MetaboX\Quantitative;

class CSV{
	protected $_delimiter;
	protected $_csvstring;
	
	protected $_nodes;
	protected $_data;
	
	public function __construct($csv_string, $delimiter = ','){
		$this->_csvstring = $csv_string;
		$this->_delimiter = $delimiter;
	}
	
	public function parse( $skip_empty_lines = true, $trim_fields = true ){
		$delimiter = $this->_delimiter;
		
	    $enc = preg_replace('/(?<!")""/', '!!Q!!', $this->_csvstring);
	    $enc = preg_replace_callback(
	        '/"(.*?)"/s',
	        function ($field) {
	            return urlencode(utf8_encode($field[1]));
	        },
	        $enc
	    );
	    $lines = preg_split($skip_empty_lines ? ($trim_fields ? '/( *\R)+/s' : '/\R+/s') : '/\R/s', $enc);
	    $data = array_map(
	        function ($line) use ($delimiter, $trim_fields) {
	            $fields = $trim_fields ? array_map('trim', explode($delimiter, $line)) : explode($delimiter, $line);
	            return array_map(
	                function ($field) {
	                    return str_replace('!!Q!!', '"', utf8_decode(urldecode($field)));
	                },
	                $fields
	            );
	        },
	        $lines
	    );
		
		$this->_nodes = $data[0];
		$this->_data  = array_slice($data, 1);
	}
	
	public function getNodes(){ return $this->_nodes; }
	public function getData(){ return $this->_data; }
	
}